package se.newton.martin.carregister;

//This is an immutable object with no setter methods and no default constructor
public class Person {
    private String name;
    private int age;

    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    @Override
    public String toString() {
        return "Name: " + name + " Age: " + age;
    }
}
